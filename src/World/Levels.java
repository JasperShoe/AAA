    private Point[] startingPositions, endingPositions;

    private Level[] levels;

    public Levels(){
        startingPositions = new Point[level_tiles.length];
        endingPositions = new Point[level_tiles.length];
        calculatePositions();

        levels = new Level[level_tiles.length];

        for(int i = 0; i < levels.length; i++){
            levels[i] = new Level(level_tiles[i], startingPositions[i], endingPositions[i]);
        }

    }

    public Level[] getLevels(){
        return levels;
    }

    public Level getLevel(int i){
        return levels[i];
    }

    public void calculatePositions(){
        for(int level = 0; level < level_tiles.length; level++){
            for(int row = 0; row < level_tiles[level].length; row++){
                if(startingPositions[level] == null && level_tiles[level][row][0] != 0){
                    startingPositions[level] = new Point(0, ((row - 1) * Anna.HEIGHT));
                }

                if(endingPositions[level] == null && level_tiles[level][row][level_tiles[level][row].length - 1] != 0){
                    endingPositions[level] = new Point(Main.WIDTH - Anna.WIDTH, ((row - 1) * Anna.HEIGHT));
                }
            }
        }
    }
